"use strict"

const numberA = parseFloat(prompt('enter your numberA'));
const numberB = parseFloat(prompt('enter your numberB'));


const addition = (numberA + numberB);
console.log(`Результат: ${numberA} + ${numberB} = ${addition}`);

const subtraction = (numberA - numberB);
console.log(`Результат: ${numberA} - ${numberB} = ${subtraction}`);

const multiplication = (numberA * numberB);
console.log(`Результат: ${numberA} * ${numberB} = ${multiplication}`);

const division = (numberA / numberB);
console.log(`Результат: ${numberA} / ${numberB} = ${division}`);


